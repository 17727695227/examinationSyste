package com.ds.model;

import java.util.Set;

/*
 * author huangzhiwei
 * 该类是保存学生提交的题目的结果
 */
public class FinishDatabase {

	private String id;// 表的id
	private String section;// 章节
	private String question;// 题目
	private String A; // A选项
	private String B;
	private String C;
	private String D;
	private String anwser;// 学生做的答案，这里志伟写错了，不想改了，将错就错
	private String result;// 题目的正确结果
	private String torf; // 学生作答是否正确（true or false），正确为“对”，错误为“错”
	private Long username;// 学生的学号

	// 考试前
	public FinishDatabase(String id, String section, String question, String a,
			String b, String c, String d) {
		super();
		this.id = id;
		this.section = section;
		this.question = question;
		A = a;
		B = b;
		C = c;
		D = d;
	}

	// 考试后
	public FinishDatabase() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSection() {
		return section;
	}

	public void setSection(String section) {
		this.section = section;
	}

	public String getQuestion() {
		return question;
	}

	public void setQuestion(String question) {
		this.question = question;
	}

	public String getA() {
		return A;
	}

	public void setA(String a) {
		A = a;
	}

	public String getB() {
		return B;
	}

	public void setB(String b) {
		B = b;
	}

	public String getC() {
		return C;
	}

	public void setC(String c) {
		C = c;
	}

	public String getD() {
		return D;
	}

	public void setD(String d) {
		D = d;
	}

	public String getAnwser() {
		return anwser;
	}

	public void setAnwser(String anwser) {
		this.anwser = anwser;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getTorf() {
		return torf;
	}

	public void setTorf(String torf) {
		this.torf = torf;
	}

	public Long getUsername() {
		return username;
	}

	public void setUsername(Long username) {
		this.username = username;
	}

}
